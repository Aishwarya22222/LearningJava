public class logicaloperators {
    public static void main(String[] args) {
        // logical AND
        System.out.println((1>2) && (5>0));
        System.out.println((2<5) && (5>2));
        System.out.println((3<2) && (8>10));

        //logical OR
        System.out.println((1>2) || (5>0));
        System.out.println((2<5) || (5>2));
        System.out.println((3<2) || (8>10));

        //logical NOT
        System.out.println ( ! (5>0)) ;
        

    }
}
